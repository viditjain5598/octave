clc; clear all; close all;
Fp=2000; Fs=5000;
Ap=2; As=42;
Ft=20000;
wp=(2*pi)*(Fp/Ft);
ws=(2*pi)*(Fs/Ft);
trwidth=ws-wp;
M=ceil(6.2*pi/trwidth)+2;
tau=(M-1)/2;
wc=(wp+ws)/2;
n=0:M-1;
hd=(sin(wc*(n-tau)))./(pi*(n-tau));
hd(tau+1)=0.35;
whann=hann(M);
h=hd.*whann;
w=0:0.01:pi;
Hw=freqz(h, 1, w);
plot(n, abs(Hw));